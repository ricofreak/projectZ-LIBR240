                        <h1>Philosphy of Managment</h1>
                        <p>When I began LIBR 204 I was asked to define ‘organizational management’ to which I responded: 

<blockquote>“Organizational management is the ability to take a goal, or set of goals, and implement them within the business system, this includes physical, virtual, and human assets.”</blockquote>

Looking back, I think I was mostly corrected but now I am able to expand that explanation of organizational management. But now I know it gets a bit more complicated than that, with several more aspects involved. Mainly, for me, that aspect is the integration of technology. Learning about technology from a management perspective was my biggest takeaway from LIBR 204. Being a technology enthusiast, I would like to explore the concept of organization management in the 21st Century world of high technology. This is especially important to us information professionals. My big question is how rapid advances in technology are affecting how information organizations are being managed. I am talking about libraries as well as other types of information organizations. </p>
                          <p>Technology management was my biggest takeaway from the course. This has a lot to do with the fact that I am always thinking about technology. Partly because it is my job but also because it is my passion. I see new and exciting technologies as a way of providing a spark to information organizations. I see it as a way of leading organizations into the future. My role as a ‘technology coordinator’ is a fun and exciting job. I do think that all technology based decisions should go through me, not the library director or the board of directors. LIBR 204 has actually shaped the way I do my job and the way I think of my job description.   </p>
                          <p>One of the most successful information organizations of the internet era is certainly Google. Google succeeded by both rifing an emerging information explosion and by using innovative management techniques. Since its origins Google competed with giants of the tech industry, mainly Microsoft. Google excelled because of their ability to envision how changes in technology would shape the future. All information organizations must strive to do the same. This is especially important for libraries where many people are beginning to question their relevance in a high tech society. After reading How Google Works by Eric Schmidt & Jonathan Rosenberg I gained even more insight into how to manage from a technology perspective. I saw my library as a mini-model of the Google model.  </p>
                          <p>When Google was started, by Larry Page and Sergey Brin, one of their first management principles was to bet on technical insights, not market research. (Schmidt & Rosenberg, 2014) If they would have just followed market trends Google would never came as far as they did. Instead they innovated, made new search algorithms, and improved user experiences online. This led to enormous success for them. Libraries and information organizations can learn a lot from this model. For instance, the library where I work could learn a lot from this and it doesn't even have to always deal with tech insights. A more general statement would be that innovation is greater than market research.</p>
                          <p>I think my library would be better off by looking into the future and offering services and programs that other libraries in the area do not. Especially in the world of technology. I would like to see Arduino, 3D printers, and MakerSpaces expanded on at the library. Something other area libraries are not doing yet. When I talk about this I get reminded of Gosling & Mintzberg’s action mindset. We want to jump on technologies like this as some as possible or risk becoming irrelevant. We also want to create technology related programs before other libraries in the area do it first. Like Google we should be leading by example. </p>
                          <p>How can a smaller information organizations learn from or adopt Google’s model? For instance Google famously implemented the ‘20% model’. Where all employees were allowed to work on whatever project they wanted to 20% of the time. This led to some of Google’s greatest products like Maps, News, and Gmail. (Schmidt & Rosenberg, 2014) But how would this work for a library with 10 staff members. We already are barely able to staff our circulation desk properly. So maybe the model needs to be tweaked to fit smaller organizations, especially those ones not chalked full of engineers and programmers who are already innovative people. 20% might not work at a small library but there is probably a similar type program that would. The point is, managers must be innovative and creative enough to adopt a model that works for their particular situation.   </p>
                          <p>Looking back I now see that organization management includes far more than just virtual, physical, and virtual resources. Managers themselves are resources. I, as a technology coordinador, am a vital resource. As that I need to always be looking towards the future and implementing new services and programs first. This way our organization can be seen as a leading innovator, even if we are a small rural library.     </p>
